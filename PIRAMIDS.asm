INCLUDE "EMU8086.INC"
.MODEL SMALL
.STACK 100H
.DATA
MSG DB "ENTER ANY NUMBER 1 TO 9 = $"
VAR DB ?
.CODE
MAIN PROC
 MOV AX, @DATA
 MOV DS, AX

 LEA DX, MSG
 MOV AH, 9
 INT 21H

 MOV AH, 1
 INT 21H
 SUB AL, 30H
 MOV VAR, AL
 PRINTN ""
 MOV CX, 0
 MOV CL, VAR
 MOV BL, 1
TOP:
 MOV CX, BX
LEVEL1:
 CMP BL, VAR
 JG EXIT
 MOV AH, 2
 MOV DL, '*'
 INT 21H
 LOOP LEVEL1
 INC BL
 PRINTN ""
 LOOP TOP
EXIT:
 MOV AH, 4CH
 INT 21H
MAIN ENDP
END MAIN